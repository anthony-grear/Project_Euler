def get_nested_digit_list(big_number,start,stop):
	"""returns a nested list containing all possible adjacent string numbers """
	nested_digit_list=[]
	while stop <1001:
		nested_digit_list.append(list(big_number[start:stop]))
		start+=1
		stop+=1

	return nested_digit_list

